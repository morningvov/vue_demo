<template>
  <div>
    <header class="site-header jumbotron">
      <div class="container">
        <div class="row">
          <div class="col-xs-12">
            <h1>请发表对Vue的评论</h1>
          </div>
        </div>
      </div>
    </header>
    <div class="container">
      <Add :addComment="addComment"/>
      <List :comments="comments" :deleteComment="deleteComment"/>
    </div>
  </div>
</template>

<script>
  import Add from './components/Add'
  import List from './components/List'

  export default {
    components: {
      Add, List
    },
    data () {
      return {
        comments: [   //数据在哪个组件，更新数据的行为（方法）就应该定义在哪个组件
          {
            name: 'bc',
            context: 'Vue 真好用'
          },
          {
            name: 'mg',
            context: 'Vue so easy'
          },
          {
            name: 'Tom',
            context: 'Vue so so'
          }
        ]
      }
    },
    methods: {
      // 添加评论
      addComment (comment) {
        this.comments.unshift(comment)  //将对象添加到数组的前面
      },
      deleteComment(index){        //根据下标删除数组中的对象
        this.comments.splice(index,1)
      }
    }
  }
</script>

